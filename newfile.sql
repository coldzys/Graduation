


          CREATE TABLE IF NOT EXISTS careerbuilder
          (
              company_name TEXT,
              location     TEXT,
              job_name     VARCHAR(256),
              update_time  VARCHAR(32),
              job_field    TEXT,
              working_form VARCHAR(128),
              salary       VARCHAR(64),
              experience   VARCHAR(32),
              level        VARCHAR(64),
              degree       VARCHAR(64),
              age          VARCHAR(32),
              sex          VARCHAR(32)
          );




